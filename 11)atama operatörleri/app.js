//! == : eşittir operatörü

console.log(5==5); //true
console.log(5==4); //false

let a = 5;
let b = 10;
let sonuc = a + b;
console.log(sonuc==11); //false

console.log(3=="3"); //true   number-string tipinde olması önemli değil

//! === : hem tip hemde değerlerinin eşit olup olmadığına bakar.

console.log(5===5); //true
console.log(3==="3"); //false

//! +=  -=  *=  /=  

let sayi = 4;

 sayi += 2 ;  //sayi = sayi + 2; 

console.log(sayi);



let say = 5;

 say -= 3; //say = say - 3;

console.log(say);



let pay = 4;

pay *= 3; //pay = pay * 3;

console.log(pay);


let key = 10;
 
key /= 2; //key = key / 2;

console.log(key);

//! %= : 

