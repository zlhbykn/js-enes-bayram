//!  +

let not1 = 60;
let not2 = 70;

console.log(not1+not2);

//!  -

let not3 = 100;
let not4 = 70;

console.log(not3-not4);

//!  *

let sayi1 = 5;
let sayi2 = 10;

console.log(sayi1*sayi2);

//!  /

let no1 = 100;
let no2 = 70;

let sonuc = (no1+no2)/2;
console.log(sonuc);

//!  %: mod alma operatörü (kalan bulma)
/*

10 / 3 = 1
12 / 5 = 2

*/

console.log(10%3);
console.log(12%5);


//!  ++ : 1 arttırmak için kullanılır

let p = 10;
p++; //11
p++; //12

console.log(p);

//!  -- : 1 azaltmak için kullanılır

let k = 8;
k--;
k--;
k--;

console.log(k);


//!  ** : bir sayının üssünü almak için

/*

2^3=8
3^2=9

*/

console.log(2**3);
console.log(3**2);
console.log(5**3);
console.log(4**3);
























